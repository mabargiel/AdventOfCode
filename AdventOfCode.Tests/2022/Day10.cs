    public void ParseRawInput_WithExampleInput_CreatesArrayOfOperations()
    {
        const string rawInput = @"noop
addx 3
addx -5";

        var input = _day10.ParseRawInput(rawInput);
        input.ShouldBe(new Instruction[]
        {
            new("noop", null),
            new("addx", 3),
            new("addx", -5)
        });
